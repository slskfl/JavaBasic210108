
public class Member {

	public String id;
	
	public Member(String id) {
		this.id=id;
	}
	
	@Override
	public boolean equals(Object obj) {
		// 매개값이 Member 타입인지 확인
		if(obj instanceof Member) {
			Member member =(Member) obj;
			if(id.equals(member.id)) {
				return true;
			}
		}
		return false;
	}
}